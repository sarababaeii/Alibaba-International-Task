import UIKit

extension UIColor {

    @nonobjc class var accent: UIColor {
    return UIColor(red: 48.0 / 255.0, green: 176.0 / 255.0, blue: 199.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var gray: UIColor {
        return UIColor(red: 91.0 / 255.0, green: 91.0 / 255.0, blue: 91.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightGray: UIColor {
        return UIColor(red: 199.0 / 255.0, green: 199.0 / 255.0, blue: 201.0 / 255.0, alpha: 1.0)
    }
}
